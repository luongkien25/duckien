void printWord(string word) {
    for (int i = 0; i < word.length(); i++) {
        word[i] = toupper(word[i]);
    }

    if (word.length() > 12) {
        cout << "invalid." << endl;
        return;
    }

    char screen[ROWS][word.length() * COLS];

    for (int i = 0; i < word.length(); i++) {
        int ascii = word[i];

        if (ascii < 'A' || ascii > 'Z') {
            cout << "invalid" << endl;
            return;
        }

        int index = ascii - 'A';
        for (int r = 0; r < ROWS; r++) {
            for (int c = 0; c < COLS; c++) {

                int x = r;
                int y = i * COLS + c;

                screen[x][y] = alphabet[index][r][c];
            }
        }
    }

    for (int r = 0; r < ROWS; r++) {
        for (int c = 0; c < word.length() * COLS; c++) {
            cout << screen[r][c];
        }
        cout << endl;
    }
}

